    d = {'height': 0}
    # Postorder traversal
    def height_helper(root, d, depth):
        if root == None:
            return
        
        # Leaf node
        height_helper(root.left, d, depth + 1)
        height_helper(root.right, d, depth + 1)
        d['height'] = max(depth, d['height'])

    height_helper(root, d, 0)
    print(d)
    return d['height']

